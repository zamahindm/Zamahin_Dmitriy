package ru.mirea.task3.shells.number2;

public class Main {
    public static void main(String[] args) {
        // Значение типа String
        String stringValue = "3.14";

        // Преобразование строки в тип double с использованием Double.parseDouble()
        double doubleValue = Double.parseDouble(stringValue);

        // Вывод значения типа double
        System.out.println("Значение типа double: " + doubleValue);
    }
}
