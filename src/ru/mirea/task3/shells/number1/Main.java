package ru.mirea.task3.shells.number1;

public class Main {
    public static void main(String[] args) {
        // Создание объектов Double с использованием метода valueOf()
        Double double1 = Double.valueOf(3.14);
        Double double2 = Double.valueOf("42.0");

        // Вывод значений объектов Double
        System.out.println("double1: " + double1);
        System.out.println("double2: " + double2);
    }
}
